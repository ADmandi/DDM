Volunteerism and social service has deep roots in India, since time immemorial, even though it received special significance during the freedom struggle through the Gandhian concept of Shramdaan, implying the offering of free labour for some noble cause like rural development, construction of social assets and public infrastructure and amenities, etc. In post-Independent India, the Non-Governmental Organisations (NGOs) have been providing support in critical sectors like health, education, water supply and sanitation, shelter and infrastructure, restoration of livelihoods, food security and nutrition, environment, etc. Given India’s multi-hazard risk and vulnerability to natural and man-made disasters, NGOs have been playing a significant role in providing humanitarian assistance to disaster-affected people in most of the severe disasters like the Latur earthquake in 1993, Orissa super cyclone in 1999, Bhuj earthquake in 2001, Indian Ocean tsunami in 2004, Kashmir earthquake in 2005, Barmer floods in 2006, Kosi floods in 2008,cyclones Aila and Laila and the more recent cloud burst in Leh in August 2010, apart from participating in providing relief to disaster affected communities in various other localised disasters.<br><br>In the recent past, the role of NGOs in disaster management (DM) has started changing from providing post-disaster relief to strengthening pre-disaster preparedness and mitigation through capacity building, public awareness campaigns, mock exercises, workshops and conferences, etc. NGOs have also started to collaborate with corporate entities in Public-Private Partnership (PPP) projects and Corporate Social Responsibility (CSR) initiatives in the field of DM at State, District and Sub-district levels. Till recently, the work of NGOs in the field of DM has been mostly sporadic, reactive, responsive and driven by local level compulsions in the geographic areas where they are implementing development projects and very often they faced enormous challenges in coordinating with the government machinery and even among NGOs themselves.<br><br>In line with it’s mandate as envisaged in the Disaster Management Act 2005 (DM Act 2005), the National Disaster Management Authority (NDMA) has circulated  National Disaster Management Guidelines on Role of NGOs in Disaster Management defining the role of NGOs in disaster management.</span>
