          <h4>Approach </h4>
          <h5>To Disaster Management</h5>
          <img src="images/11.jpg" alt="blog-image" />
          <span>Till recently, the approach to Disaster Management has been reactive and relief centric. A paradigm shift has now taken place at the national level from the relief centric syndrome to holistic and integrated approach with emphasis on prevention, mitigation and preparedness. These efforts are aimed to conserve developmental gains as also minimize losses to lives, livelihood and property.<br><br>
            <h5>Section 2 (e) of the Disaster Management Act 2005 defines disaster management as follows:</h5>Disaster Management means a continuous and integrated process of planning, organising, coordinating and implementing measures which are necessary or expedient for :<br>
1) Prevention of danger or threat of any disaster.<br>
2) Mitigation or reduction of risk of any disaster or its severity or consequences.<br>
3) Capacity-building.<br>
4) Preparedness to deal with any disaster.<br>
5) Prompt response to any threatening disaster situation or disaster.<br>
6) Assessing the severity or magnitude of effects of any disaster.<br>
7) Evacuation, Rescue and Relief.<br>
8) Rehabilitation and Reconstruction.<br>
The HPC, which was constituted in August 1997 under the Chairmanship of Sh.J.C.Pant. HPC members were drawn from the Ministries, States, NGOs and experts from relevant fields. It was the first attempt in India towards evolving a systematic, comprehensive and holistic approach towards all disasters. The original mandate of the HPC was confined to the preparation of management plans for natural disasters only. However, it was expanded to include man-made disasters as well in order to develop an effective plan of action that would encompass disasters of all origins and shades. The Terms of Reference of the HPC were subsequently enlarged to include man- made disasters also with the approval of Prime Minister of India. Representation from concerned Ministries dealing with industrial, nuclear, biological, chemical disasters were ensured by way of inclusion of experts from these Ministries.
The HPC thus constituted five sub-groups to go into details of five major classifications as decided by the HPC.<br><br>
<h5>SUB GROUP I-  WATER AND CLIMATE RELATED HAZARDS</h5>
(a) Floods and drainage management<br>
(b) Cyclones, tornadoes, Hurricanes<br>
(c) Hailstorms, cloud bursts, Snow Avalanches, Heat and Clod Waves Thunder & Lightening<br>
(d) Sea Erosion<br>
(e) Droughts<br><br>
<h5>SUB GROUP-II- GEOLOGICALLY RELATED HAZARDS</h5>
(a) Earthquakes<br>
(b) Landslide, Mudflows<br>
(c) Soil Erosion<br>
(d) Dam Bursts and Dam Failures<br>
(e) Mine Fires<br><br>
<h5>SUB GROUP-III CHEMICAL, INDUSTRIAL AND NUCLEAR RELATED DISASTERS</h5>
(a) Chemical and Industrial Disasters<br>
(b) Nuclear Disasters<br><br>
<h5>SUB GROUP –IV ACCIDENT RELATED DISASTERS</h5>
(a) Road, Rail and other transportation accidents including Waterways.<br>
(b) Mine Flooding<br>
(c) Major Building Collapse<br>
(d) Serial Bomb Blasts<br>
(e) Festival related Disasters<br>
(f) Urban Fires<br>
(g) Oil Spill<br>
(h) Village Fires<br>
(i) Boat Capsizing<br>
(j) Forest Fires<br>
(k) Electrical Disasters and Fires<br><br>
<h5>SUB GROUP-V BIOLOGICALLY RELATED DISASTERS</h5>
(a) Biological Disasters<br>
(b) Food Poisoning<br>
(c) Cattle Epidemics/Pest Attacks<br>
