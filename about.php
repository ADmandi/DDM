          <h2 class="title-style-2">ABOUT DDMA <span class="title-under"></span></h2>

          <p>
            The Disaster Management Act, 2005, (23 December 2005) No. 53 of 2005, was passed by the Rajya Sabha, the upper house of the Parliament of India on 28 November, and by the Lok Sabha, the lower house of the Parliament, on 12 December 2005. It received the assent of The President of India on 9 January 2006. The Disaster Management Act, 2005 has 11 chapters and 79 sections.The Act extends to the whole of India. The Act provides for "the effective management of disasters and for matters connected therewith or incidental thereto."
          </p>
          <h2 class="title-style-2">National Authority <span class="title-under"></span></h2>
          
          <p>
            The Act calls for the establishment of National Disaster Management Authority (NDMA), with the Prime Minister of India as chairperson. The NDMA may have no more than nine members including a Vice-Chairperson.The tenure of the members of the NDMA shall be five years. The NDMA which was initially established on 30 May 2005 by an executive order, was constituted under Section-3(1) of the Disaster Management Act, on 27 September 2006.The NDMA is responsible for "laying down the policies, plans and guidelines for disaster management" and to ensure "timely and effective response to disaster". Under section 6 of the Act it is responsible for laying "down guidelines to be followed by the State Authorities in drawing up the State Plans"
          </p>
          <h2 class="title-style-2">National Executive Committee <span class="title-under"></span></h2>
          <p>
            The Act under Section 8 enjoins the Central Government to Constitute a National Executive Committee (NEC) to assist the National Authority. The NEC is composed of Secretary level officers of the Government of India in the Ministries of home, agriculture, atomic energy, defence, drinking water supply, environment and forests, finance (expenditure), health, power, rural development, science and technology, space, telecommunication, urban development, and water resources, with the Home secretary serving as the Chairperson, ex officio.
          </p>
          <h2 class="title-style-2">District Disaster Management Authority <span class="title-under"></span></h2>
          <p>
            The Chairperson of District Disaster Management Authority (DDMA) will be the Collector or District Magistrate or Deputy Commissioner of the district. The elected representative of the area is member of the DDMA as an ex officio co-Chairperson, (Section 25).
          </p>
              
