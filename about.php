          <span><h5>Section 18. Powers and functions of State Authority.</h5>
(1) Subject to the provisions of this Act, a State Authority shall have the responsibility for laying down policies and plans for disaster management in the State.<br>
(2) Without prejudice to the generality of provisions contained in sub-section (1), the State Authority may-<br>
(a) Lay down the State disaster management policy.<br>
(b) Approve the State Plan in accordance with the guidelines laid down by the National Authority.<br>
(c) Approve the disaster management plans prepared by the departments of the Government of the State.<br>
(d) Lay down guidelines to be followed by the departments of the Government of the State for the purposes of integration of measures for prevention of disasters and mitigation in their development plans and projects and provide necessary technical assistance therefore.<br>
(e) Coordinate the implementation of the State Plan.<br>
(f) Recommend provision of funds for mitigation and preparedness measures.<br>
(g) Eeview the development plans of the different departments of the State and ensure that prevention and mitigation measures are integrated therein.<br>
(h) Review the measures being taken for mitigation, capacity building and preparedness by the departments of the Government of the State and issue such guidelines as may be necessary.<br>
(3) The Chairperson of the State Authority shall, in the case of emergency, have power to exercise all or any of the powers of the State Authority but the exercise of such powers shall be subject to ex post facto ratification of the State Authority".<br><br>

<h5>Section 19 of the Act is reproduced as unders:</h5>
Further as per Section 19 of the Act the SDMA is also to lay down guidelines for minimum standard of relief by State Authority. 
Section 19. Guidelines for minimum standard of relief by State Authority. - The State Authority shall lay down detailed guidelines for providing standards of relief to persons affected by disaster in the State, Provided that such standards shall in no case be less than the minimum standards in the guidelines laid down by the National Authority in this regard.<br>
The Act also provides for State Executive Committee (SEC) for the State. The SEC was also notified on 1.6.2007 headed by the Chief Secretary and others as members to assist the SDMA in performing the functions prescribed by the Act. SEC is the executive arm of the SDMA. Section 22 of the Act mandates the State Executive Committee (SEC) with certain responsibilities. The sum up of the responsibilities of the SDMA and SEC are as under:<br>

Key Functions Expected:
(a) Development of awareness campaign strategy and its implementation in the state.<br>
(b) Development of Human Resource Plan for implementation thereof  – development of training modules and material
(c) Preparation or updation of state disaster management plan to ensure that the issues of DRR have been addressed<br>
(d) To initiating risk and vulnerability assessments and preparation of annual vulnerability and risk reduction reports.<br>
(e) Laying down guidelines to integrate DRR into development process<br>
(f) Follow up with various line departments to ensure that DRR issues have been addressed in their development plans.<br>
(g) Preparation of Disaster Risk Reduction Projects in various sectors.<br>
(h) Carrying out DRR Audit of the development plans prepared by line departments.<br>
(i) Developing a Recovery framework for the state.<br>
(j) Development of Knowledge and information sharing platform in DRR<br>
(k) Conceptualizing and formulating projects and programmes as a part of the national initiatives/schemes.<br>
(m) Preparation of Minimum Standards of Relief<br>
(n) Preparation of Disaster Management Policy<br>
(l) Preparation of Mitigation plans vis a vis various hazards<br>
(o) Coordinate and monitor the implementation of National Policy, National Plan and State Plan<br>
(p) Lay down guidelines for the preparation of DMP by various departments<br>
(q) Lay down guidelines for safe construction practices and ensure compliance thereof
(r) Provide necessary technical assistance or give advice to District Authorities.<br>
(s) Lay down, review and update state level response plans and guidelines<br>
(u) Ensuring the communication system is in order – setting up and strengthening of EOCs<br>
(v) Ensuring the conduct of mock drills regularly.
