				<p>Always follow the correct procedures. Never take shortcuts. Take responsibility and clean up if you made a mess. Clean and organize your workspace. Ensure a clear and easy route to emergency exits and equipment. Be alert and awake on the job. Be attentive at all times to your work surroundings. When in doubt, contact your supervisor or manager for instruction, guidance, or training. Never take risks when it comes to safety. Obey safety signs, stickers, and tags. Take short breaks when you keep up a repetitive motion for a long period of time, and sit, stand, or walk with good posture. Report serious injuries immediately to a supervisor and get emergency assistance. Keep things in perspective. Hazards may be limitless, so focus on the most likely risks first.</p><br>




<b>*Before flooding occurs.</b><br><br>
                  <p>a)All your family members should know the safe route to nearest shelter/ raised pucca
house.<br>
b) If your area is flood-prone, consider alternative building materials. Mud walls are
more likely to be damaged during floods. You may consider making houses where the
walls are made of local bricks up to the highest known flood level with cement
pointing.<br>
c) Have an emergency kit on hand which includes a:<br>
1) A portable radio, torch and spare batteries;<br>
2) Stocks of fresh water, dry food (chura, mudi, gur, biscuits), kerosene, candle
and matchboxes;<br>
3) Waterproof or polythene bags for clothing and valuables, an umbrella and
bamboo stick (to protect from snake), salt and sugar.<br>
4) A first aid kit, manual and strong ropes for tying things.
</p><br><br>
<b>When you hear a flood warning or if flooding appears likely.</b><br><br>
<p> a)Tune to your local radio/TV for warnings and advice.<br>
1)Keep vigil on flood warning given by local authorities.<br>
2) Don’t give any importance to rumours and don’t panic.<br>
3) Keep dry food, drinking water and clothes ready.<br>
b) Prepare to take bullock carts, other agricultural equipments, and domestic animals to
safer places or to higher locations.<br>
c) Plan which indoor items you will raise or empty if water threatens to enter your house
d) Check your emergency kit. </p><br><br>
<b>*During floods</b><br><br>
<p>a)Drink boiled water.<br>
b) Keep your food covered, don’t take heavy meals.<br>
c) Use raw tea, rice-water, tender coconut-water, etc. during diarrhoea; contact your
  ANM/AWW for ORS and treatment.<br>
 d)Do not let children remain on empty stomach.<br>
e) Use bleaching powder and lime to disinfect the surrounding.<br>
f) Help the officials/volunteers distributing relief materials.
</p><br><br>
<b>If you need to evacuate</b><br><br>
<p>a)Firstly pack warm clothing, essential medication, valuables, personal papers, etc. in
waterproof bags, to be taken with your emergency kit.<br>
b) Take the emergency kit.<br>
c) Inform the local volunteers (if available), the address of the place you are evacuating
to.<br>
d) Raise furniture, clothing and valuables onto beds, tables and to the top of the roof
(electrical items highest).<br>
e) Turn off power.<br>
f) Whether you leave or stay, put sandbags in the toilet bowl and over all laundry /
bathroom drain-holes to prevent sewage back-flow.<br>
g) Lock your home and take recommended/known evacuation routes for your area.<br>
h) Do not get into water of unknown depth and current. </p><br><br>
<b>If you stay or on your return
</b><br><br>
<p>a)Stay tuned to local radio for updated advice.<br>
b) Do not allow children to play in, or near, flood waters.<br>
c) Avoid entering floodwaters. If you must, wear proper protection for your feet
and check depth and current with a stick. Stay away from drains, culverts and
water over knee-deep.<br>
d) Do not use electrical appliances, which have been in floodwater until checked for
safety.<br>
e) Do not eat food, which has been in floodwaters.<br>
f) Boil tap water (in cities) until supplies have been declared safe. In case of rural
areas, store tube well water in plastic jars or use halogen tablets before drinking.<br>
