				<p>Always follow the correct procedures. Never take shortcuts. Take responsibility and clean up if you made a mess. Clean and organize your workspace. Ensure a clear and easy route to emergency exits and equipment. Be alert and awake on the job. Be attentive at all times to your work surroundings. When in doubt, contact your supervisor or manager for instruction, guidance, or training. Never take risks when it comes to safety. Obey safety signs, stickers, and tags. Take short breaks when you keep up a repetitive motion for a long period of time, and sit, stand, or walk with good posture. Report serious injuries immediately to a supervisor and get emergency assistance. Keep things in perspective. Hazards may be limitless, so focus on the most likely risks first.</p><br>
 <b> 1. Prepare your family.</b><br><br>
                 <b> *Before The earthquake.</b><br>
                  <p>
            Now is the time to formulate a safety plan for you and your family. If you wait
until the earth starts to shake, it may be too late. Consider the following safety
measures:<br><br>
a) Always keep the following in a designated place: bottled drinking water, nonperishable
food (chura, gur, etc), first-aid kit, torchlight and battery-operated
radio with extra batteries.<br>
b) Teach family members how to turn off electricity, gas, etc.<br>
c) Identify places in the house that can provide cover during an earthquake.<br>
d) It may be easier to make long distance calls during an earthquake.
Identify an out-of-town relative or friend as your family’s emergency contact. If the
family members get separated after the earthquake and are not able to
contact each other, they should contact the designated relative/friend. The
address and phone number of the contact person/relative should be with all
the family members.<br><br>
<b>*If outdoors:</b>
<p>
            a) Move into the open, away from buildings, streetlights, and utility wires. Once
in the open, stay there until the shaking stops.<br>
b) If your home is badly damaged, you will have to leave. Collect water, food,
medicine, other essential items and important documents before leaving.<br>
c) Avoid places where there are loose electrical wires and do not touch metal
objects that are in touch with the loose wires.<br>
d) Do not re-enter damaged buildings and stay away from badly damaged
structures.<br><br></p>
<b>*If in a moving vehicle:</b>
<p>
            Move to a clear area away from buildings, trees, overpasses, or utility wires,
stop, and stay in the vehicle. Once the shaking has stopped, proceed with
caution. Avoid bridges or ramps that might have been damaged by the quake. <br><br>
          </p>





<b>*Safeguard your house</b>
<p>
            a) Consider retrofitting your house with earthquake-safety measures.Reinforcing the foundation and frame could make your house quake resistant.You may consult a reputable contractor and follow building codes.<br>
            b) Kutchha buildings can also be retrofitted and strengthened.<br><br>

          </p>
          <b>*During quake</b>
          <p>
            Earthquakes give no warning at all. Sometimes, a loud rumbling sound might
signal its arrival a few seconds ahead of time. Those few seconds could give you
a chance to move to a safer location. Here are some tips for keeping safe during
a quake.<br>

Take cover. Go under a table or other sturdy furniture; kneel, sit, or stay close
to the floor. Hold on to furniture legs for balance. Be prepared to move if your
cover moves.<br>
a) If no sturdy cover is nearby, kneel or sit close to the floor next to a structurally
sound interior wall. Place your hands on the floor for balance.<br>
b) Do not stand in doorways. Violent motion could cause doors to slam and
cause serious injuries. You may also be hit be flying objects.<br>
c) Move away from windows, mirrors, bookcases and other unsecured heavy
objects.<br>
d) If you are in bed, stay there and cover yourself with pillows and blankets.<br>
e) Do not run outside if you are inside. Never use the lift.<br>
f) If you are living in a kutcha house, the best thing to do is to move to an open
area where there are no trees, electric or telephone wires.<br><br>
                
<b>*After the quake</b>
<p>
            Here are a few things to keep in mind after an earthquake. The caution you
display in the aftermath can be essential for your personal safety.<br>
a) Wear shoes/chappals to protect your feet from debris.<br>
b) After the first tremor, be prepared for aftershocks. Though less intense,
aftershocks cause additional damages and may bring down weakened
structures. Aftershocks can occur in the first hours, days, weeks, or even
months after the quake.<br>
c) Check for fire hazards and use torchlights instead of candles or lanterns.<br>
d) If the building you live in is in a good shape after the earthquake, stay inside
and listen for radio advises. If you are not certain about the damage to your
building, evacuate carefully. Do not touch downed power line.<br>
e) Help injured or trapped persons. Give first aid where appropriate. Do not
move seriously injured persons unless they are in immediate danger of further
injury. In such cases, call for help.<br>
f) Remember to help your neighbours who may require special assistanceinfants,
the elderly, and people with disabilities.<br>
g) Listen to a battery-operated radio for the latest emergency information.
 Stay out of damaged buildings.<br>
h) Return home only when authorities say it is safe. Clean up spilled medicines,
bleaches or gasoline or other flammable liquids immediately. Leave the area
if you smell gas or fumes from other chemicals. Open closet and cupboard
doors cautiously.<br>
i) If you smell gas or hear hissing noise, open windows and quickly leave the
building. Turn off the switch on the top of the gas cylinder.<br>
j) Look for electrical system damages - if you see sparks, broken wires, or if
you smell burning of amber, turn off electricity at the main fuse box. If you
have to step in water to get to the fuse box, call an electrician first for advice.<br>
k) Check for sewage and water lines damage. If you suspect sewage lines are
damaged, avoid using the toilets. If water pipes are damaged, avoid using
water from the tap.<br>
l) Use the telephone only for emergency calls.<br>
m) In case family members are separated from one another during an
earthquake (a real possibility during the day when adults are at work and
children are at school), develop a plan for reuniting after the disaster. Ask an
out of state / district relative or friend to serve as the “family contact”. Make
sure everyone in the family knows the name, address, and phone number(s)
of the contact person (s).
