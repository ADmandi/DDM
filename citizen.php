				<li class="list-group-item"><h4>(C) LandSlide Safty Tips</h4><br>
				<p>Always follow the correct procedures. Never take shortcuts. Take responsibility and clean up if you made a mess. Clean and organize your workspace. Ensure a clear and easy route to emergency exits and equipment. Be alert and awake on the job. Be attentive at all times to your work surroundings. When in doubt, contact your supervisor or manager for instruction, guidance, or training. Never take risks when it comes to safety. Obey safety signs, stickers, and tags. Take short breaks when you keep up a repetitive motion for a long period of time, and sit, stand, or walk with good posture. Report serious injuries immediately to a supervisor and get emergency assistance. Keep things in perspective. Hazards may be limitless, so focus on the most likely risks first.</p><br>
 <b> 1. Prepare your family.</b><br><br>


 <b>*During a Landslide:</b><br><br>
                 <p> a)Stay alert and awake. Many debris-flow fatalities occur when people are sleeping. Listen
to a Weather Radio or portable, battery-powered radio or television for warnings of
intense rainfall. Be aware that intense, short bursts of rain may be particularly dangerous,
especially after longer periods of heavy rainfall and damp weather.<br>
b) If you are in areas susceptible to landslides and debris flows, consider leaving if it is safe
to do so. Remember that driving during an intense storm can be hazardous. If you remain
at home, move to a second story if possible. Staying out of the path of a landslide or
debris flow saves lives.<br>
c) Listen for any unusual sounds that might indicate moving debris, such as trees cracking
or boulders knocking together. A trickle of flowing or falling mud or debris may precede
larger landslides. Moving debris can flow quickly and sometimes without warning.<br>
d) If you are near a stream or channel, be alert for any sudden increase or decrease in water
flow and for a change from clear to muddy water. Such changes may indicate landslide
activity upstream, so be prepared to move quickly. Don't delay! Save yourself, not your
belongings.<br>
e) Be especially alert when driving. Embankments along roadsides are particularly
susceptible to landslides. Watch the road for collapsed pavement, mud, fallen rocks, and
other indications of possible debris flows. 
</p><br>
<b>*What to Do if You Suspect Imminent Landslide Danger:</b><br><br>
<p>a)Contact your local fire, police, or public works department. Local officials are the best
persons able to assess potential danger.<br>
b) Inform affected neighbors. Your neighbors may not be aware of potential hazards.<br>
Advising them of a potential threat may help save lives. Help neighbors who may need
assistance to evacuate.<br>
c) Evacuate. Getting out of the path of a landslide or debris flow is your best protection</p><br>

<b>*Media and Community Education Ideas:</b><br><br>
<p>a)In an area prone to landslides, publish a special newspaper section with emergency
information on landslides and debris flows. Localize the information by including the
phone numbers of local emergency services offices, the Red Cross, and hospitals.<br>
b) Report on what city and county governments are doing to reduce the possibility of
landslides. Interview local officials about local land- use zoning regulations.<br>
c) Interview local officials and major insurers. Find out if debris flow is covered by flood
insurance policies and contact your local emergency management office to learn more
about the program.<br>
d) Work with local emergency services to prepare special reports for people with mobility
impairments on what to do if evacuation is ordered.<br>
e) Support your local government in efforts to develop and enforce land-use and building
ordinances that regulate construction in areas susceptible to landslides and debris flows.
Buildings should be located away from steep slopes, streams and rivers, intermittentstream
channels, and the mouths of mountain channels.  </p><br>


<b>*After the Landslide:</b><br><br>
<p>a)Stay away from the slide area. There may be danger of additional slides.
b) Check for injured and trapped persons near the slide, without entering the direct slide
area. Direct rescuers to their locations.<br>
c) Help a neighbor who may require special assistance - infants, elderly people, and people
with disabilities. Elderly people and people with disabilities may require additional
assistance. People who care for them or who have large families may need additional
assistance in emergency situations.<br>
d) Listen to local radio or television stations for the latest emergency information.
e) Watch for flooding, which may occur after a landslide or debris flow. Floods sometimes
follow landslides and debris flows because they may both be started by the same event.
f) Look for and report broken utility lines to appropriate authorities. Reporting potential
hazards will get the utilities turned off as quickly as possible, preventing further hazard
and injury.<br>
g) Check the building foundation, chimney, and surrounding land for damage. Damage to
foundations, chimneys, or surrounding land may help you assess the safety of the area.
h) Replant damaged ground as soon as possible since erosion caused by loss of ground
cover can lead to flash flooding.<br>
i) Seek the advice of a geotechnical expert for evaluating landslide hazards or designing
corrective techniques to reduce landslide risk. A professional will be able to advise you
of the best ways to prevent or reduce landslide risk, without creating further hazard.
 </p><br>

<b>*Media and Community Education Ideas:</b><br><br>
<p>a)· In an area prone to landslides, publish a special newspaper section with emergency
information on landslides and debris flows. Localize the information by including the
phone numbers of local emergency services offices, the American Red Cross chapter, and
hospitals.<br>
b) Report on what city and county governments are doing to reduce the possibility of
landslides. Interview local officials about local land- use zoning regulations.<br>
c) Interview local officials and major insurers regarding the National Flood Insurance
Program. Find out if debris flow is covered by flood insurance policies from the National
Flood Insurance Program and contact your local emergency management office to learn
more about the program.<br>
d) Work with local emergency to prepare special reports for people with mobility
impairments on what to do if evacuation is ordered.<br>
e) Support your local government in efforts to develop and enforce land-use and building
ordinances that regulate construction in areas susceptible to landslides and debris flows.
Buildings should be located away from steep slopes, streams and rivers, intermittentstream
channels, and the mouths of mountain channels. 
 </p><br>

 <b>*Before a Landslide: How to Plan:</b><br><br>
<p>a)·Develop a Family Disaster Plan. Please see the "Family Disaster Plan" section for general family
planning information. Develop landslide-specific planning.<br>
b)Learn about landslide risk in your area. Contact local officials, state geological surveys or
departments of natural resources, and university departments of geology. Landslides occur where
they have before, and in identifiable hazard locations. Ask for information on landslides in your
area, specific information on areas vulnerable to landslides, and request a professional referral
for a very detailed site analysis of your property, and corrective measures you can take, if
necessary.
 
 </p><br>
 <b>*If you are at risk from landslides:</b><br><br>
<p>a)Talk to your insurance agent.<br>
b) Develop an evacuation plan.<br>
c) Discuss landslides and debris flow with your family. Everyone should know what to do
in case all family members are not together. Discussing disaster ahead of time helps
reduce fear and lets everyone know how to respond during a landslide or debris flow.
 
 </p><br>



