Take cover. Go under a table or other sturdy furniture; kneel, sit, or stay close
to the floor. Hold on to furniture legs for balance. Be prepared to move if your
cover moves.<br>
a) If no sturdy cover is nearby, kneel or sit close to the floor next to a structurally
sound interior wall. Place your hands on the floor for balance.<br>
b) Do not stand in doorways. Violent motion could cause doors to slam and
cause serious injuries. You may also be hit be flying objects.<br>
c) Move away from windows, mirrors, bookcases and other unsecured heavy
objects.<br>
d) If you are in bed, stay there and cover yourself with pillows and blankets.<br>
e) Do not run outside if you are inside. Never use the lift.<br>
