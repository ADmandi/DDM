<b>*Areas that are generally prone to landslides</b><br><br>
                 <p>a)Old and/or recent existing landslides.<br>
b) Base or top of slopes.<br>
c) Base of minor drainage hollows.<br>
d) Base or top of an old fill slope.<br>
e) Base or top of a steep cut slope.<br>
</p>
<b>*Areas generally safe from landslides</b><br>
<p>a)Hard, non-jointed bedrock that has not moved in the past.<br>
b) Flat-lying areas away from slopes and steep river banks.<br>
c) The nose of ridges, set back from the tops of slopes.</p><br>

<b>*Landslide warning signs</b><br>
<p>a)Sticking or jamming of doors or windows.<br>
b) Appearance of cracks in plaster, tile, brick, or foundations.<br>
c) Pulling away from the building of outside walls or stairs.<br>
d) Slow development of widening cracks on the ground or on paved areas such as streets.<br>
e) Breakage of underground utility lines.<br>
f) Appearance of bulging ground at the base of a slope.<br>
g) Emergence of flowing ground water in new sites.<br>
h) Sudden decrease in creek water levels though rain is still falling or just recently stopped.<br>
i) Tilting or moving of fences, retaining walls, utility poles, or trees.<br>
j) Faint rumbling sound that increases in volume as the landslide nears.The ground slopes
downward in one specific direction and may begin shifting in that direction under your
feet. </p><br>
<b>*Immediate steps for imminent Landslide</b><br>
<p>a)Contact Contact your local fire, police or public works department.<br>
b) Inform affected neighbors.<br>
c) Leave the area quickly. </p><br>

<b>*Actions to be taken before Intense Rainfall</b><br>
<p>a)Become familiar with the land around you. Slopes where landslides or debris flows have
occurred in the past are likely to experience them in the future.<br>
b) Buildings should be located away from known landslides, debris flows, steep slopes,<br>
streams and rivers, intermittent-stream channels, and the mouths of mountain channels.<br>
c) Observe the patterns of storm-water drainage on slopes near your home, and watch
especially the places were runoff water converges, increasing flow over soil-covered 
slopes. Observe the hillsides around your home for any signs of land movement, such as
small landslides or debris flows or progressively tilting trees.<br>
d) Contact your local authorities to learn about the disaster management response, and
develop your own emergency plans for your family and business.</p><br>

<b>*During Intense Rainfall</b><br>
<p>a)Be observant. Many landslide and debris flow fatalities occur when people are sleeping.
Listen to radio for warnings of intense rainfall. Intense short bursts of rain may be
particularly dangerous, especially after longer periods of heavy rainfall and damp
weather.<br>
b) Unusual sounds might indicate moving debris, such as trees cracking or boulders
knocking together. A trickle of flowing or falling mud or debris may precede larger
landslides. Be alert for any sudden increase or decrease in water flow in streams or
channels. Such changes may indicate landslide activity upstream, so be prepared to
move quickly.<br>
c) If you live in areas susceptible to landslides and debris flows, consider leaving if it is safe
to do so. If you remain at home, move to a part of the house farthest away from the
source of the landslide or debris flows, such as an upper floor, but keep an escape route
open should it become necessary to leave the house.<br>
d) Be alert when on the roads. Embankments along roadsides are particularly susceptible to
landslides. Watch the road for collapsed pavement, mud, fallen rocks, and other
indications of possible landslides or debris flows. 
</p><br>

<b>*After Intense Rainfall</b><br>
<p>a) Be alert for signs indicating land movement. Landslides can occur weeks or months after
intense storms. 
</p>

<b>*Things to Remember </b><br>
<p>a)Mudflows tend to flow in channels, but will often spread out over a floodplain. They
generally occur in places where they have occurred before.<br>
b) Landslide and mudflows usually strike without warning. The force of rocks, soil, or other
debris moving down a slope can devastate anything in its path. Take the following steps
to be ready.<br>
c) Plant ground cover on slopes and build retaining walls.<br>
d) In mudflow areas, build channels or deflection walls to direct the flow around buildings.
d) Remember: If you build walls to divert debris flow and the flow lands on a neighbor's
property, you may be liable for damages.
</p><br>

<b>*Precautions to be taken during landslides</b><br><br>
<b>If inside a building:</b><br>
<p>a)Stay inside.<br>
b) Take cover under a desk, table, or other piece of sturdy furniture. </p><br>

<b>*If outdoors: </b><br>
<p>a)Try and get out of the path of the landslide or mudflow.<br>
b) Run to the nearest high ground in a direction away from the path.<br>
c) If rocks and other debris are approaching, run for the nearest shelter such as a group of
trees or a building.<br>
d) If escape is not possible, curl into a tight ball and protect your head. </p><br>

<b>*After Landslide</b><br>
<p>a)Stay away from the slide area. There may be danger of additional slides.<br>
b) Check for injured and trapped persons near the slide area. Give first aid if trained.<br>
c) Remember to help your neighbors who may require special assistance--infants, elderly
people, and people with disabilities.<br>
d) Listen to a battery-operated radio or television for the latest emergency information.<br>
e) Remember that flooding may occur after a mudflow or a landslide.<br>
f) Check for damaged utility lines. Report any damage to the utility company.<br>
g) Check the building foundation, chimney, and surrounding land for damage.<br>
h) Replant damaged ground as soon as possible since erosion caused by loss of ground
cover can lead to flash flooding. 
</p><br><br>


